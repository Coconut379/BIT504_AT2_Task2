/**
 * Enumeration for the player's moves in the Tic-Tac-Toe game.
 * This enum defines the possible states of a cell on the game board.
 */
public enum Player {
    Empty,  // The cell is empty, no player's move
    Cross,  // The cell is occupied by player 'X'
    Nought  // The cell is occupied by player 'O'
}
